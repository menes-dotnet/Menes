namespace Menes.PetStore.Responses
{
    /// <summary>
    /// The size of the pet.
    /// </summary>
    public enum Size
    {
        /// <summary>
        /// A pet smaller than a great dane
        /// </summary>
        Small,

        /// <summary>
        /// A pet as large as a great dane, but smaller than a horse
        /// </summary>
        Medium,

        /// <summary>
        /// A pet as large as, or larger than, a horse
        /// </summary>
        Large,
    }
}
